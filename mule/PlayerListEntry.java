package mule;

public class PlayerListEntry {
	public String name;
	public int race;
	public int color;
	public boolean ready;
	public int playerNumber;
	public PlayerListEntry(String name, int race, int color, int playerNumber){
		this.name = name;
		this.race = race;
		this.color = color;
		this.playerNumber = playerNumber;
		ready = false;
	}
}
